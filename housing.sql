-- Create the database
CREATE DATABASE IF NOT EXISTS Housing;
USE Housing;

-- Drop existing tables if they exist
DROP TABLE IF EXISTS Rooms;
DROP TABLE IF EXISTS Residents;
DROP TABLE IF EXISTS Houses;
DROP TABLE IF EXISTS Campus;

CREATE TABLE Campus (
    Campus_id INT AUTO_INCREMENT PRIMARY KEY,
    Num_houses INT,
    Num_residents INT,
    Location VARCHAR(100)
);

CREATE TABLE Houses (
    House_name VARCHAR(100) PRIMARY KEY,
    Campus_id INT,
    Location VARCHAR(200),
    Num_residents INT,
    Num_rooms INT,
    FOREIGN KEY (Campus_id) REFERENCES Campus(Campus_id)
);

CREATE TABLE Residents (
    Resident_id INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100),
    Class VARCHAR(10),
    GPA DECIMAL(3, 2),
    House_name VARCHAR(100),
    Email VARCHAR(100),
    Phone VARCHAR(20),
    Balance DECIMAL(10, 2),
    FOREIGN KEY (House_name) REFERENCES Houses(House_name)
) AUTO_INCREMENT = 101;

CREATE TABLE Rooms (
    Room_id VARCHAR(10) PRIMARY KEY,
    House_name VARCHAR(100),
    Current_residents INT,
    Room_type VARCHAR(50),
    FOREIGN KEY (House_name) REFERENCES Houses(House_name)
);



